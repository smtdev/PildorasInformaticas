package com.frenetyklok.video21;

public class UsoBucleFor {

	public static void main(String[] args) {
		
		for(int i = 0; i < 10; i++){
			System.out.println("Pablo");
		}
		
		System.out.println("También hacia atrás!");
		
		for(int i = 10; i > 0; i--){
			System.out.println("Pablo");
		}
		
		System.out.println("Otra forma de hacer lo mismo:");
		
		for(int i = 0; i < 20; i += 2){
			System.out.println("Pablo");
		}

	}

}

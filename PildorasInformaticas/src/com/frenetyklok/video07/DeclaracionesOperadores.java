package com.frenetyklok.video07;

public class DeclaracionesOperadores {

	public static void main(String[] args) {
		
		int a = 5;
		int b;
		
		b = 7; //aquí no tiene sentido utilizar dos líneas en la declaración de la variable.
		
		int c = b + a;
		
		c++;
		c += 6;
		
		System.out.println(c);
		
		
		int d = 16;
		int g = 3;
		
		double f = d / g; // si queremos que conserve decimlaes numerador y/o denominador tienen que tenerlos
		
		System.out.println(f);
		

	}

}

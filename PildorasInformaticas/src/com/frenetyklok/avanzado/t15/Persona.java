package com.frenetyklok.avanzado.t15;

public class Persona {
	
	private int dni;
	private int edad;
	
	public Persona(int dni, int edad){
		this.dni = dni;
		this.edad = edad;
	}

	public int getDni() {
		return dni;
	}

	public void setDni(int dni) {
		this.dni = dni;
	}

	public int getEdad() {
		return edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}
	
	

}
